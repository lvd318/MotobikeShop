    public void BaoCao2(){
        DefaultTableModel dtm = (DefaultTableModel) tblBaoCao2.getModel();
        dtm.setNumRows(0);
        Connection conn = ConnectDB.createCon();
        Statement statement = null;
        
        try{          
            String sql = "select xe.*, sum(ct.SOLUONG) as sldaban from XE xe, HOADON hd, CTHD ct \n"
                    + "where xe.MAXE = ct.MAXE and hd.MAHD = ct.MAHD and\n"
                    + "cast(strftime('%m', NGAYLAP) as Integer) =  (cast(strftime('%m', 'now') as Integer) - 1) \n"
                    + "group by xe.MAXE order by sldaban desc";
            statement = conn.createStatement();
            
            ResultSet rs = statement.executeQuery(sql);
            int i = 0;
            while(rs.next()){
                i++;
                if(i>3)
                    break;
                dtm.addRow(new Object[]{rs.getString(1), rs.getString(2),rs.getString(3), rs.getInt(8), rs.getInt(5),
                rs.getInt(6), rs.getString(7)}); 
            }
            this.tblBaoCao2.setModel(dtm);
        }catch(SQLException ex){
            ex.printStackTrace();
        }
        
        if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException ex) {
                }
            }
    }
