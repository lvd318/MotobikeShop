    private void jComboBoxThangItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxThangItemStateChanged
        String sqlString = "select sum(ct.SOLUONG)*(xe.GIABAN) as doanhthu, sum(ct.SOLUONG)*(xe.GIABAN - xe.GIANHAP) as loinhuan\n" +
                           "from HOADON as hd, CTHD as ct, XE as xe\n" +
                            "where hd.MAHD = ct.MAHD and ct.MAXE = xe.MAXE and\n" +
                            "cast(strftime('%m', hd.NGAYLAP) as Integer) = ?";
        Connection conn = ConnectDB.createCon();
        Statement statement = null;
        
        try{
            PreparedStatement pres = conn.prepareStatement(sqlString);
            pres.setString(1, jComboBoxThang.getSelectedItem().toString());
            
            ResultSet rs = pres.executeQuery();
            if(rs.getString(1) == null){
                txtDoanhThu.setText("0");
                txtLoiNhuan.setText("0");
            }else{
                txtDoanhThu.setText(rs.getString(1));
                txtLoiNhuan.setText(rs.getString(2));
            }
        }catch(SQLException ex){
            ex.printStackTrace();
        }
        
        if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException ex) {
                }
            }
                
    }//GEN-LAST:event_jComboBoxThangItemStateChanged


